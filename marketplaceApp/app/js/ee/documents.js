//creates the download link for files from Alfresco
function createLinkForDocument(url, theLocale, linkText, defaultText)
{
	var link ="";
	var textToUse=defaultText;
	if(!isEmpty(linkText))
	{
		textToUse= linkText;
	}
	
	
	url = createURLForDocument(url, theLocale);
	
	link= '<a href="'+url+'">'+ textToUse +'</a>';
	
	return link;
}


function createURLForDocument(url, theLocale)
{
 
	if(isEmpty(url))
	{
		url = "#";
	}
	else
	{	
		//link to download
		url= "/ee-rest/auth/ffe/"+theLocale+"/DocumentManagementEE/DocumentFromECM?fileIdentifier=" + url;
	}
 	
	return url;
}